package com.example.project_2_student.Models;

public class Program {
    String day , first , second , third , forth , fifth , sixth , seventh ;

    public String getDay() {
        return day;
    }

    public String getFirst() {
        return first;
    }

    public String getSecond() {
        return second;
    }

    public String getThird() {
        return third;
    }

    public String getForth() {
        return forth;
    }

    public String getFifth() {
        return fifth;
    }

    public String getSixth() {
        return sixth;
    }

    public String getSeventh() {
        return seventh;
    }

    public Program(String day, String first, String second, String third, String forth, String fifth, String sixth, String seventh) {
        this.day = day;
        this.first = first;
        this.second = second;
        this.third = third;
        this.forth = forth;
        this.fifth = fifth;
        this.sixth = sixth;
        this.seventh = seventh;
    }
}
